package com.ipt.ebsa.agnostic.client.skyscape.exception;

public class VAppUnstableStatusControlException extends ControlException {


	/**
	 * 
	 */
	private static final long serialVersionUID = 5022241078140775230L;

	public VAppUnstableStatusControlException() {
		super();
	}

	public VAppUnstableStatusControlException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public VAppUnstableStatusControlException(String message, Throwable cause) {
		super(message, cause);
	}

	public VAppUnstableStatusControlException(String message) {
		super(message);
	}

	public VAppUnstableStatusControlException(Throwable cause) {
		super(cause);
	}
	
}
