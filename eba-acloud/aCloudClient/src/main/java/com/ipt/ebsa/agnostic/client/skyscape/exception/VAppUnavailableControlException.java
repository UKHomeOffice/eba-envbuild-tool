package com.ipt.ebsa.agnostic.client.skyscape.exception;

public class VAppUnavailableControlException extends ControlException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5210679388852796029L;

	public VAppUnavailableControlException() {
		super();
	}

	public VAppUnavailableControlException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public VAppUnavailableControlException(String message, Throwable cause) {
		super(message, cause);
	}

	public VAppUnavailableControlException(String message) {
		super(message);
	}

	public VAppUnavailableControlException(Throwable cause) {
		super(cause);
	}
	
}
